package com.hyc.bean;

public class MacEntity {
	//MAC地址
	String mac;
	
	//设备编码（唯一标识）
	String devices = null;
	



	public String getDevices() {
		return devices;
	}

	public void setDevices(String devices) {
		this.devices = devices;
	}

	public String getMac() {
		return mac;
	}

	public void setMac(String mac) {
		this.mac = mac;
	}


	
}
