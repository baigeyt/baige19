package com.hyc.bean;

//apk信息实体类
public class APKInfo {
	//apk版本名
	String apkVN;
	//apk下载路径
	String apkUrl;
	//apk上传时间
	String apkTime;
	public String getApkVN() {
		return apkVN;
	}
	public void setApkVN(String apkVN) {
		this.apkVN = apkVN;
	}
	public String getApkUrl() {
		return apkUrl;
	}
	public void setApkUrl(String apkUrl) {
		this.apkUrl = apkUrl;
	}
	public String getApkTime() {
		return apkTime;
	}
	public void setApkTime(String apkTime) {
		this.apkTime = apkTime;
	}

}
